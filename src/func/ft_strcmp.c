#include "functions.h"

int	ft_strcmp(char *s1, char *s2)
{
	int	result;
	int	i;

	result = 0;
	i = 0;
	while (s1[i] != '\0' && s2[i] != '\0' && !result)
	{
		result = s1[i] - s2[i];
		i++;
	}
	if (s1[i] == '\0' && s2[i] != '\0')
		return (-100);
	else if (s2[i] == '\0' && s1[i] != '\0')
		return (100);
	return (result);
}
